int	ft_isalnum(int c)
{
	int	output;

	output = 0;
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		output = 1;
	else if (c >= '0' && c <= '9')
		output = 1;
	return (output);
}
